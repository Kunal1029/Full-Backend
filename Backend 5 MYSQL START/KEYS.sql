-- What are keys?

Keys are special columns in the table

#PRIMARY Key
it is a column (or set of columns) in a table that uniquely identifies each row. (a uniue id)
There is only 1PK & it should be not null.

#FOREIGN Key

A FK is a column (or set of column) in a table that refers to the PK in another table.
FKs can have duplicate & null values.
There can be multiple FKs.
